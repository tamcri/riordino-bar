insert into public.inventory_excel_preset_items (preset_id, item_id)
select
  p.id,
  i.id
from preset p
join codes c on true
join public.items i
  on upper(trim(i.code)) = upper(trim(c.code))
where i.is_active = true
on conflict (preset_id, item_id) do nothing;
